            <p>
                Union Internationale pour la Fraternité et la Solidarité  est une Organisation Non Gouvernementale à but non lucratif créée en Août 2011 en Côte d'Ivoire dans la ville de Bouaké par M. OUGA BI V. GASTON et de deux (2) Co-fondateurs dont un professeur de mathématiques du nom de M. KAMBIRÉ Antoine Marie et M. KOUAKOUA N'Guessan Eritier. Sa première vision lors de sa création était d'intervenir dans le domaine éducatif.
            </p>
            <p>
                        Elle a pour mission principale l’éducation et pour objectifs de :
                <ul>
                    <li>Développer l'esprit entrepreneurial chez les jeunes africains.</li>
                    <li>Fournir des bourses d’études nationales et internationales aux élèves et étudiants démunis, orphelins, défavorisés et séropositifs.</li>
                    <li>Lutter contre la déscolarisation des élèves et étudiants.</li>
                    <li>Assister les élèves et étudiants qui veulent ou qui sont obligés d’abandonner leurs études par faute de moral ou de moyens financiers en leur évitant plus tard dans la société des vices (vols, banditisme, drogue, prostitution...)</li>
                    <li>Lutter en donnant à ses élèves et étudiants les possibilités et les moyens naturels ou financiers pour qu’ils puissent continuer leurs études pour pouvoir s’instruire et avoir une place dans la société.</li>
                    <li>Alphabétiser les jeunes filles et les femmes analphabètes.</li>
                    <li>Prendre en charge les élèves et étudiants affectés par le VIH/SIDA.</li>
                    <li>Lutter contre le VIH/SIDA et les IST dans le milieu scolaire et universitaire.</li>
                    <li>Promouvoir et protéger les droits de la jeune fille et de la femme.</li>
                    <li>Construire et réhabiliter des infrastructures éducatives.</li>
                </ul>
